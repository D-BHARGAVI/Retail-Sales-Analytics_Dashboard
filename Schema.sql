CREATE TABLE sales(

row_id INT,

order_id VARCHAR(50),

order_date DATE,

ship_date DATE,

ship_mode VARCHAR(50),

customer_id VARCHAR(50),

customer_name VARCHAR(100),

segment VARCHAR(50),

country VARCHAR(50),

city VARCHAR(100),

state VARCHAR(100),

postal_code VARCHAR(20),

region VARCHAR(50),

product_id VARCHAR(50),

category VARCHAR(100),

sub_category VARCHAR(100),

product_name TEXT,

sales FLOAT,

quantity INT,

discount FLOAT,

profit FLOAT

);